			<h3>Introduction</h3>
				<p>Hello! My name is Irfan Bashir. I am a sophomore and an international student from Indian administered part of Kashmir pursuing a BA in Financial Economics at the University of Rochester, New York.</p>

			<h3>Work Experience</h3>
				<p>I have worked as a Journalist since 2009 and have been actively associated with major media publications in Kashmir. Currently I am working as a part time Editor for Kashmir Scan News Magazine. I am also employed by the Rettner Media Lab as a Digital Media Specialist at the University of Rochester. On campus, I'm actively involved with social and cultural organizations. I am currently the VP of Finance and Philanthropy Chair for the Kappa Chapter of Sigma Beta Rho Fraternity Inc. I have also held various other positions in the Fraternity including Programming and IFC (Interfraternity Fraternity Council) chairs.</p>

				<p>I won the Newly Inducted Brother of the Year National Award for the Sigma Beta Rho Fraternity Inc. in 2016. I was the winner of the Anti-Racism Video Contest for the 'We Are Better Than' That campaign by the University; the video was used by the university to raise awareness about racism. I also won a Student Life Award in 2016 for Creative Collaboration for my event 'Islamophobia' as the programming chair of my Fraternity.</p>

				<p>In December 2016, I co-founded Federation for Peace and Development (FPD), a non-profit organization aimed to uplift the less privileged people of the society. In over a year, we have conducted various educational workshops and writing and painting competitions across many parts of Kashmir. We have also raised awareness about Drug Abuse by closely working with the Drug De-addiction center in Srinagar, Kashmir and held workshops to help drug addicts recover and find their place in the society.</p>

			<h3>Hobbies and Interests</h3>
				<p>I like to write poetry and short stories. I am currently working on writing a book which I hope to publish by the end of the year. My other interests include painting, wood carving and photography. In my spare time I like to read and listen to Sufi Music.</p>

